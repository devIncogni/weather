export default async function getProcessedWeatherData(location, unitGroup) {
  const totalWeatherData = await getWeatherData(location, unitGroup);

  try {
    const {
      latitude,
      longitude,
      resolvedAddress,
      description,
      currentConditions,
      alerts,
    } = totalWeatherData;

    const dayData = [];

    for (let index = 0; index < 7; index += 1) {
      const currentDay = totalWeatherData.days[index];

      if (index === 0) {
        const { datetimeEpoch, tempmin, tempmax, conditions, icon, hours } =
          currentDay;
        dayData.push({
          datetimeEpoch,
          tempmin,
          tempmax,
          conditions,
          icon,
          hours,
        });
      } else {
        const { datetimeEpoch, tempmin, tempmax, conditions, icon } =
          currentDay;
        dayData.push({ datetimeEpoch, tempmin, tempmax, conditions, icon });
      }
    }

    return {
      latitude,
      longitude,
      resolvedAddress,
      description,
      currentConditions,
      alerts,
      dayData,
    };
  } catch (error) {
    console.error(error);
    return null;
  }
}
